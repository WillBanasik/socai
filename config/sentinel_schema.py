def get_ip_tables(allowed: list[str] | None = None) -> list[tuple]:
    """Tables with IPv4 IOC fields."""
    registry = _load_registry()
    results = []
    for table, info in sorted(registry.items()):
        if table in _SKIP_TABLES:
            continue
        if allowed and table not in allowed:
            continue
        cols = info["columns"]
        ip_fields = [c for c in cols if c in _IP_FIELDS]
        if not ip_fields:
            continue
        project = _build_project(cols, ip_fields)
        results.append((table, ip_fields, project, _table_description(table)))
    return results


