# ---------------------------------------------------------------------------
# Registry cache
# ---------------------------------------------------------------------------
_registry: dict | None = None


def _load_registry() -> dict:
    global _registry
    if _registry is not None:
        return _registry
    if not _SCHEMA_PATH.exists():
        _registry = {}
        return _registry
    with open(_SCHEMA_PATH) as f:
        _registry = json.load(f)
    return _registry


def _build_project(table_cols: dict, ioc_fields: list[str], max_extra: int = 5) -> str:
    """Build a project column string: IOC fields + context columns."""
    cols = []
    # Always start with TimeGenerated if present
    if "TimeGenerated" in table_cols:
        cols.append("TimeGenerated")
    # Add IOC fields
    for f in ioc_fields:
        if f not in cols:
            cols.append(f)
    # Add context columns
    for ctx in _CONTEXT_PRIORITY:
        if ctx in table_cols and ctx not in cols:
            cols.append(ctx)
            if len(cols) >= len(ioc_fields) + max_extra + 1:
                break
    return ", ".join(cols)


