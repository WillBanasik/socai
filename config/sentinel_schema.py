def get_all_table_names() -> list[str]:
    """All table names in the registry."""
    return sorted(_load_registry().keys())


def get_table_columns(table_name: str) -> dict[str, str]:
    """Column name → type mapping for a specific table."""
    registry = _load_registry()
    entry = registry.get(table_name, {})
    return entry.get("columns", {})


def get_table_workspaces(table_name: str) -> list[str]:
    """Workspace codes where this table exists."""
    registry = _load_registry()
    entry = registry.get(table_name, {})
    return entry.get("workspaces", [])


def has_registry() -> bool:
    """Check if sentinel_tables.json exists and has data."""
    registry = _load_registry()
    return len(registry) > 0
